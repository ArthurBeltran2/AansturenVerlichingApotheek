/**
  * @brief  Interrupt routine when edge is detected at input 1 or 4
  * To the inputs a toggle button is connected. when button is toggled,
  * function checks input level for high or low
  * depending the status the corresponding output is activated or deactivated.
  * activation is done by setting variable INxStatus
  * This INxStatus is used by the TIM2  to see if the corresponding output should be PWMm'd or not
  * @retval None
  */

void ExtI_IN0_IN4_Callback(void)
{
	/*In case of rising edge IN0 or IN4 Status is ACTIVE else */
	/*In case of falling edge IN0 or IN4 Status is INACTIVE*/
	IN0Status = LL_GPIO_IsInputPinSet(IN0_GPIO_Port, IN0_Pin); /*Read level of input pin0 into IN0Status variable*/
	IN4Status = LL_GPIO_IsInputPinSet(IN4_GPIO_Port, IN4_Pin); /*Read level of input pin4 into IN4Status variable*/

	if ((IN0Status == ACTIVE) || (IN4Status == ACTIVE)) /*True in case INO or I4 is High*/
	{
		if (LL_TIM_IsEnabledIT_CC1(TIM2)==0)  		/*In case channel 1 CC register of TIM2 not enabled.*/
		{											/*occurs when both outputs where previous OFF or LOW*/

			LL_TIM_OC_SetCompareCH1(TIM2,CH1RiseCount); /*Set CH1 first Rising edge to correct value*/
			/*Enable interrupt CC*/
			LL_TIM_EnableIT_CC1(TIM2);				/* Enable the capture/compare interrupt for channel 1*/
			/*start CH1 of TIM2*/
			LL_TIM_EnableIT_CC1(TIM2);				/*enable CC1 timer2*/
		}
	}
	/*When both inputs are LOW the TIM2 CH1 can stop counting*/
	if ((IN0Status == INACTIVE) && (IN4Status == INACTIVE))
	{
		LL_TIM_DisableIT_CC1(TIM2);			/*disable CC1 timer2*/
	}
}
