	CH3RiseCount = (CH2FallCount + OffsetCount) % ReloadCounter;
	CH3FallCount = (CH3RiseCount + DutyCycleCount) % ReloadCounter;

	CH4RiseCount = (CH3FallCount + OffsetCount) % ReloadCounter;
	CH4FallCount = (CH4RiseCount + DutyCycleCount) % ReloadCounter;
