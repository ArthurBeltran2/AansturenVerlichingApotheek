/**
  * @brief TIM2 CC Interrupt routine , activated when match between counter of TIM2 with CC register CH1.
  *
  * TIM2 is an up counter.
  * The counter counts till it matches the value in the auto reload register set in the TIM2 init routine
  * after the match the counter resets to 0. (no interrupts are generated)
  *
  * This interrupt routine generates the PWM / Dutycycle signal of output CH0 and CH4
  * The PWM signal is broken up in different Event Cycles.
  * In this case 2 event cycles during the whole TIM2 count.
  * Each event cycle defines the number of clock counts the signal needs to be high or low
  * One event cycles defines the high period of the PWM signal
  * One event cycle defines the low period of the PWM signal
  * The high periode is counted in number of TIM2 clock cycles and calculated in the TIM2CountValues-Init
  * The low periode is counted in number of TIM2  clock cycles and calculated in the TIM2CountValues-Init

  * Whether the TIM2 CCx counts and whether an output pin is set depends on the variable INxStatus
  * This INxStatus is set in the EXTI_IN0_IN4_callback routine
  * @retval None
  */

void TIM2CaptureCompareCH1_Callback(void)
{
	//Test signal
	LL_GPIO_TogglePin(LD3_GPIO_Port,LD3_Pin); //channel A on Scope

    if (TIM2CH1_EventCycle == 0)
    {
    	/*Set CH1 OC value to next matching value = CH1FallCount*/
    	LL_TIM_OC_SetCompareCH1(TIM2,CH1FallCount);

  	    /* Set Output Active High */
  	    if (IN0Status==ACTIVE) { LL_GPIO_SetOutputPin(OUT0_GPIO_Port,OUT0_Pin);}
  	    if (IN4Status==ACTIVE) { LL_GPIO_SetOutputPin(OUT4_GPIO_Port,OUT4_Pin);}
  	    TIM2CH1_EventCycle++;

    }
    else if (TIM2CH1_EventCycle >= 1)
	{
    	/*Set CH1 OC value to next matching value = CH1RiseCount*/
    	LL_TIM_OC_SetCompareCH1(TIM2,CH1RiseCount);

    	/* Set Output Active Low */
    	LL_GPIO_ResetOutputPin(OUT0_GPIO_Port,OUT0_Pin);
    	LL_GPIO_ResetOutputPin(OUT4_GPIO_Port,OUT4_Pin);
    	TIM2CH1_EventCycle = 0;
    }
}

